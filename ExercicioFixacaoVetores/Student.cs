using System;

namespace ExercicioFixacaoVetores
{
    internal class Student
    {
        string name;
        string email;
        int room;

        public string Name
        {
            get { return name; }
            set { name = value; }
        }
        public string Email
        {
            get { return email; }
            set { email = value; }
        }
        public int Room
        {
            get { return room; }
            set { room = value; }
        }
    }
}
